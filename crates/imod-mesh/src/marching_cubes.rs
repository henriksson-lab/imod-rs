#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn empty_below_threshold() {
        // All values below threshold -> no triangles.
        let vol = vec![0.0f32; 8];
        let mesh = marching_cubes(&vol, 2, 2, 2, 0.5);
        assert!(mesh.indices.is_empty());
    }

    #[test]
    fn full_above_threshold() {
        // All values above threshold -> no triangles (fully inside).
        let vol = vec![1.0f32; 8];
        let mesh = marching_cubes(&vol, 2, 2, 2, 0.5);
        assert!(mesh.indices.is_empty());
    }

    #[test]
    fn single_corner() {
        // One corner above -> one triangle.
        let mut vol = vec![0.0f32; 8];
        vol[0] = 1.0; // corner (0,0,0) above threshold
        let mesh = marching_cubes(&vol, 2, 2, 2, 0.5);
        assert!(!mesh.indices.is_empty());
        assert_eq!(mesh.indices.len() % 3, 0);
        assert_eq!(mesh.vertices.len(), mesh.normals.len());
    }

    #[test]
    fn sphere_produces_closed_surface() {
        // Small sphere in a 10x10x10 grid.
        let n = 10;
        let mut vol = vec![0.0f32; n * n * n];
        let c = n as f32 / 2.0;
        for z in 0..n {
            for y in 0..n {
                for x in 0..n {
                    let dx = x as f32 - c;
                    let dy = y as f32 - c;
                    let dz = z as f32 - c;
                    vol[z * n * n + y * n + x] = 1.0 / (1.0 + (dx * dx + dy * dy + dz * dz).sqrt());
                }
            }
        }
        let mesh = marching_cubes(&vol, n, n, n, 0.3);
        assert!(mesh.indices.len() > 0);
        assert_eq!(mesh.indices.len() % 3, 0);
    }
}
