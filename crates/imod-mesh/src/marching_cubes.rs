//! Marching cubes isosurface extraction (Lorensen & Cline, 1987).
//!
//! Given a 3-D scalar volume stored as a flat row-major `&[f32]` array and an
//! iso-value threshold, this module produces a triangle mesh that approximates
//! the isosurface.

/// Triangle mesh produced by [`marching_cubes`].
pub struct IsosurfaceMesh {
    /// Vertex positions `[x, y, z]`.
    pub vertices: Vec<[f32; 3]>,
    /// Per-vertex normals `[nx, ny, nz]` estimated from the volume gradient.
    pub normals: Vec<[f32; 3]>,
    /// Triangle indices (every three consecutive values form one triangle).
    pub indices: Vec<u32>,
}

/// Extract an isosurface from a row-major 3-D volume at the given threshold.
///
/// The volume is indexed as `volume[z * ny * nx + y * nx + x]`.
pub fn marching_cubes(
    volume: &[f32],
    nx: usize,
    ny: usize,
    nz: usize,
    threshold: f32,
) -> IsosurfaceMesh {
    assert_eq!(volume.len(), nx * ny * nz, "volume length mismatch");

    let mut vertices: Vec<[f32; 3]> = Vec::new();
    let mut normals: Vec<[f32; 3]> = Vec::new();
    let mut indices: Vec<u32> = Vec::new();

    // Helper: sample the volume (clamped).
    let sample = |x: usize, y: usize, z: usize| -> f32 {
        volume[z * ny * nx + y * nx + x]
    };

    // Helper: gradient by central differences (clamped at boundaries).
    let gradient = |x: usize, y: usize, z: usize| -> [f32; 3] {
        let gx = if x > 0 && x + 1 < nx {
            (sample(x + 1, y, z) - sample(x - 1, y, z)) * 0.5
        } else if x + 1 < nx {
            sample(x + 1, y, z) - sample(x, y, z)
        } else if x > 0 {
            sample(x, y, z) - sample(x - 1, y, z)
        } else {
            0.0
        };
        let gy = if y > 0 && y + 1 < ny {
            (sample(x, y + 1, z) - sample(x, y - 1, z)) * 0.5
        } else if y + 1 < ny {
            sample(x, y + 1, z) - sample(x, y, z)
        } else if y > 0 {
            sample(x, y, z) - sample(x, y - 1, z)
        } else {
            0.0
        };
        let gz = if z > 0 && z + 1 < nz {
            (sample(x, y, z + 1) - sample(x, y, z - 1)) * 0.5
        } else if z + 1 < nz {
            sample(x, y, z + 1) - sample(x, y, z)
        } else if z > 0 {
            sample(x, y, z) - sample(x, y, z - 1)
        } else {
            0.0
        };
        [gx, gy, gz]
    };

    // Walk every cell (cube) in the volume.
    for z in 0..nz - 1 {
        for y in 0..ny - 1 {
            for x in 0..nx - 1 {
                // Eight corner values.
                let v = [
                    sample(x, y, z),
                    sample(x + 1, y, z),
                    sample(x + 1, y + 1, z),
                    sample(x, y + 1, z),
                    sample(x, y, z + 1),
                    sample(x + 1, y, z + 1),
                    sample(x + 1, y + 1, z + 1),
                    sample(x, y + 1, z + 1),
                ];

                // Cube index from corner classification.
                let mut cube_index: usize = 0;
                for i in 0..8 {
                    if v[i] >= threshold {
                        cube_index |= 1 << i;
                    }
                }

                let edge_bits = EDGE_TABLE[cube_index];
                if edge_bits == 0 {
                    continue;
                }

                // Corner positions (relative to cell origin).
                let pos: [[f32; 3]; 8] = [
                    [x as f32, y as f32, z as f32],
                    [x as f32 + 1.0, y as f32, z as f32],
                    [x as f32 + 1.0, y as f32 + 1.0, z as f32],
                    [x as f32, y as f32 + 1.0, z as f32],
                    [x as f32, y as f32, z as f32 + 1.0],
                    [x as f32 + 1.0, y as f32, z as f32 + 1.0],
                    [x as f32 + 1.0, y as f32 + 1.0, z as f32 + 1.0],
                    [x as f32, y as f32 + 1.0, z as f32 + 1.0],
                ];

                // Corner grid coords for gradient lookup.
                let gc: [[usize; 3]; 8] = [
                    [x, y, z],
                    [x + 1, y, z],
                    [x + 1, y + 1, z],
                    [x, y + 1, z],
                    [x, y, z + 1],
                    [x + 1, y, z + 1],
                    [x + 1, y + 1, z + 1],
                    [x, y + 1, z + 1],
                ];

                // Edge endpoint pairs (Lorensen convention).
                const EDGE_CONN: [[usize; 2]; 12] = [
                    [0, 1], [1, 2], [2, 3], [3, 0],
                    [4, 5], [5, 6], [6, 7], [7, 4],
                    [0, 4], [1, 5], [2, 6], [3, 7],
                ];

                // Interpolate vertices on each active edge.
                let mut edge_verts: [u32; 12] = [0; 12];
                for e in 0..12 {
                    if edge_bits & (1 << e) == 0 {
                        continue;
                    }
                    let [a, b] = EDGE_CONN[e];
                    let va = v[a];
                    let vb = v[b];
                    let t = if (vb - va).abs() > 1e-10 {
                        (threshold - va) / (vb - va)
                    } else {
                        0.5
                    };
                    let t = t.clamp(0.0, 1.0);
                    let vx = pos[a][0] + t * (pos[b][0] - pos[a][0]);
                    let vy = pos[a][1] + t * (pos[b][1] - pos[a][1]);
                    let vz = pos[a][2] + t * (pos[b][2] - pos[a][2]);

                    // Interpolate gradient for normal.
                    let ga = gradient(gc[a][0], gc[a][1], gc[a][2]);
                    let gb = gradient(gc[b][0], gc[b][1], gc[b][2]);
                    let mut nx_ = ga[0] + t * (gb[0] - ga[0]);
                    let mut ny_ = ga[1] + t * (gb[1] - ga[1]);
                    let mut nz_ = ga[2] + t * (gb[2] - ga[2]);
                    let len = (nx_ * nx_ + ny_ * ny_ + nz_ * nz_).sqrt();
                    if len > 1e-10 {
                        // Negate so normal points from high to low (outward from surface).
                        nx_ = -nx_ / len;
                        ny_ = -ny_ / len;
                        nz_ = -nz_ / len;
                    }

                    edge_verts[e] = vertices.len() as u32;
                    vertices.push([vx, vy, vz]);
                    normals.push([nx_, ny_, nz_]);
                }

                // Emit triangles from the tri table.
                let row = &TRI_TABLE[cube_index];
                let mut i = 0;
                while i < 16 && row[i] != -1 {
                    indices.push(edge_verts[row[i] as usize]);
                    indices.push(edge_verts[row[i + 1] as usize]);
                    indices.push(edge_verts[row[i + 2] as usize]);
                    i += 3;
                }
            }
        }
    }

    IsosurfaceMesh {
        vertices,
        normals,
        indices,
    }
}

